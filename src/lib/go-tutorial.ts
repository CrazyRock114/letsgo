// 获取教程的扁平步骤列表（带章节信息）
export function getAllSteps(): (LessonStep & { chapterId: string; chapterName: string; chapterEmoji: string; stepIndex: number; totalSteps: number })[] {
  const result: (LessonStep & { chapterId: string; chapterName: string; chapterEmoji: string; stepIndex: number; totalSteps: number })[] = [];
  let globalIdx = 0;
  for (const ch of GO_TUTORIAL) {
    for (let i = 0; i < ch.steps.length; i++) {
      result.push({
        ...ch.steps[i],
        chapterId: ch.id,
        chapterName: ch.chapter,
        chapterEmoji: ch.emoji,
        stepIndex: globalIdx,
        totalSteps: 0,
      });
      globalIdx++;
    }
  }
  // Fill total
  const total = result.length;
  return result.map(s => ({ ...s, totalSteps: total }));
}

// 按难度获取章节
export function getChaptersByDifficulty(level: 1 | 2 | 3): LessonChapter[] {
  return GO_TUTORIAL.filter(ch => ch.difficulty === level);
}

// 获取总步骤数
export function getTotalSteps(): number {
  return GO_TUTORIAL.reduce((sum, ch) => sum + ch.steps.length, 0);
}
